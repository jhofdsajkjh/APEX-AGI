#[cfg(test)]
mod tests {
    use super::*;
    use crate::inference::MockEngine;

    #[tokio::test]
    async fn test_store_and_recall() {
        let embedder = Arc::new(MockEngine::new());
        let memory = LongTermMemory::new(
            MemoryConfig {
                db_path: String::new(),
                ..Default::default()
            },
            embedder,
            "test-agent",
        );

        let id = memory.store("Hello world", MemoryType::Episodic).await;
        assert!(id.starts_with("mem-test-agent"));

        let recent = memory.recent(10).await;
        assert_eq!(recent.len(), 1);
        assert_eq!(recent[0].content, "Hello world");
    }

    #[tokio::test]
    async fn test_buffer_size_limit() {
        let embedder = Arc::new(MockEngine::new());
        let memory = LongTermMemory::new(
            MemoryConfig {
                episodic_buffer_size: 3,
                db_path: String::new(),
                ..Default::default()
            },
            embedder,
            "test-agent",
        );

        for i in 0..5 {
            memory
                .store(format!("Entry {}", i), MemoryType::Episodic)
                .await;
        }

        let recent = memory.recent(10).await;
        assert_eq!(recent.len(), 3);
        assert_eq!(recent[0].content, "Entry 4");
    }

    #[tokio::test]
    async fn test_search_filter_by_type() {
        let embedder = Arc::new(MockEngine::new());
        let memory = LongTermMemory::new(
            MemoryConfig {
                db_path: String::new(),
                ..Default::default()
            },
            embedder,
            "test-agent",
        );

        memory.store("A fact", MemoryType::Semantic).await;
        memory.store("An event", MemoryType::Episodic).await;

        let results = memory
            .search(&MemoryQuery {
                memory_type: Some(MemoryType::Semantic),
                top_k: 10,
                ..Default::default()
            })
            .await;

        assert_eq!(results.len(), 1);
        assert_eq!(results[0].content, "A fact");
    }

    #[tokio::test]
    async fn test_importance_estimation() {
        let embedder = Arc::new(MockEngine::new());
        let memory = LongTermMemory::new(
            MemoryConfig {
                db_path: String::new(),
                ..Default::default()
            },
            embedder,
            "test-agent",
        );

        // Error content should get higher importance
        let id1 = memory
            .store_tagged(
                "Critical error: system crash",
                MemoryType::Episodic,
                0.9,
                vec!["error".into()],
            )
            .await;
        let id2 = memory
            .store_tagged("Routine log entry", MemoryType::Episodic, 0.2, vec![])
            .await;

        let results = memory
            .search(&MemoryQuery {
                top_k: 10,
                ..Default::default()
            })
            .await;
        assert_eq!(results.len(), 2);
        // High importance first
        assert_eq!(results[0].id, id1);
    }

    #[tokio::test]
    async fn test_context_hybrid() {
        let embedder = Arc::new(MockEngine::new());
        let memory = LongTermMemory::new(
            MemoryConfig {
                db_path: String::new(),
                ..Default::default()
            },
            embedder,
            "test-agent",
        );

        for i in 0..5 {
            memory
                .store(format!("Recent item {}", i), MemoryType::Episodic)
                .await;
        }

        let ctx = memory.context("test", 3).await;
        assert_eq!(ctx.len(), 3);
        // Most recent first
        assert_eq!(ctx[0].content, "Recent item 4");
    }
}
